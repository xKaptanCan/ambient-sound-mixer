class I18n {
    constructor() {
        this.currentLanguage = this.loadLanguage();
    }

    loadLanguage() {
        const saved = localStorage.getItem('zenith-language');
        if (saved && LANGUAGES[saved]) {
            return saved;
        }

        // Default to English as requested, overriding browser detection for first visit
        return 'en';
    }

    saveLanguage(lang) {
        localStorage.setItem('zenith-language', lang);
        this.currentLanguage = lang;
    }

    t(key, fallback = null) {
        const translation = TRANSLATIONS[this.currentLanguage]?.[key];
        if (translation) return translation;

        // Fallback to English
        if (this.currentLanguage !== 'en') {
            return TRANSLATIONS.en?.[key] || fallback || key;
        }

        return fallback || key;
    }

    setLanguage(lang) {
        if (!LANGUAGES[lang]) return;

        this.saveLanguage(lang);
        this.applyTranslations();
    }

    applyTranslations() {
        // Text content
        document.querySelectorAll('[data-i18n]').forEach(el => {
            try {
                const key = el.dataset.i18n;
                const translation = this.t(key);
                if (!translation || translation === key) return;

                // Proper emoji detection using spread operator (handles surrogate pairs)
                const currentText = el.textContent.trim();
                const chars = [...currentText]; // Properly splits emojis
                const firstChar = chars[0] || '';

                // Check if first char is an emoji (codepoint > 255 typically means non-ASCII)
                if (firstChar && firstChar.codePointAt(0) > 255) {
                    el.textContent = firstChar + ' ' + translation;
                } else {
                    el.textContent = translation;
                }
            } catch (err) {
                console.error('Failed to translate element:', el, err);
            }
        });

        // Placeholders
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            try {
                const key = el.dataset.i18nPlaceholder;
                const translation = this.t(key);
                if (translation) el.placeholder = translation;
            } catch (e) { }
        });

        // Titles
        document.querySelectorAll('[data-i18n-title]').forEach(el => {
            try {
                const key = el.dataset.i18nTitle;
                const translation = this.t(key);
                if (translation) el.title = translation;
            } catch (e) { }
        });
    }

    getLanguages() {
        return LANGUAGES;
    }

    getCurrentLanguage() {
        return this.currentLanguage;
    }
}

